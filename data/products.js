export const products = [...archiveProducts, ...futureProducts];

export const featuredProduct = archiveProducts[0];
export const featuredFutureProduct = futureProducts[0];

export const productBySlug = (slug) => products.find(p => p.slug === slug);
export const getProductsByEra = (era) => products.filter(p => p.era === era);

export const getLineage = (slug) => {
  const product = productBySlug(slug);
  if (!product) return null;
  if (product.era === "archive" && product.futureLineage) {
    return { from: product, to: productBySlug(product.futureLineage) };
  }
  if (product.era === "future" && product.archiveLineage) {
    return { from: productBySlug(product.archiveLineage), to: product };
  }
  return null;
};
